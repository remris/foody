/// Gibt [IngredientNutrients] für eine Katalog-Zutat zurück.
IngredientNutrients? getNutrientsForCatalogEntry(IngredientEntry entry) {
  final values = _nutrientsByCanonicalId[entry.canonicalId];
  if (values == null) return null;
  return IngredientNutrients(
    kcalPer100g:    values[0],
    proteinPer100g: values[1],
    fatPer100g:     values[2],
    carbsPer100g:   values[3],
    fiberPer100g:   values.length > 4 ? values[4] : null,
    sugarPer100g:   values.length > 5 ? values[5] : null,
  );
}

/// Sucht Nährwerte anhand des Zutatennamen im Katalog.
/// Fallback wenn kein InventoryItem mit nutrientInfo vorhanden.
IngredientNutrients? getNutrientsForIngredientName(String name) {
  final entry = IngredientCatalog.findByName(name);
  if (entry == null) return null;
  return getNutrientsForCatalogEntry(entry);
}

